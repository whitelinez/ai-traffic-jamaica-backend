-- Functions
CREATE OR REPLACE FUNCTION place_bet_atomic(
  p_user_id UUID,
  p_round_id UUID,
  p_market_id UUID,
  p_amount INT,
  p_potential_payout INT
) RETURNS JSON
LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE
  v_balance INT;
  v_bet_id UUID;
BEGIN
  INSERT INTO user_balances (user_id, balance)
  VALUES (p_user_id, 1000)
  ON CONFLICT (user_id) DO NOTHING;

  SELECT balance INTO v_balance
  FROM user_balances
  WHERE user_id = p_user_id
  FOR UPDATE;

  IF v_balance < p_amount THEN
    RETURN json_build_object('error', 'Insufficient balance');
  END IF;

  UPDATE user_balances
  SET balance = balance - p_amount
  WHERE user_id = p_user_id;

  INSERT INTO bets (
    user_id, round_id, market_id, amount, potential_payout, status, bet_type
  )
  VALUES (
    p_user_id, p_round_id, p_market_id, p_amount, p_potential_payout, 'pending', 'market'
  )
  RETURNING id INTO v_bet_id;

  UPDATE markets
  SET total_staked = total_staked + p_amount
  WHERE id = p_market_id;

  RETURN json_build_object('bet_id', v_bet_id);
END;
$$;

CREATE OR REPLACE FUNCTION get_user_balance(p_user_id UUID)
RETURNS INT
LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO user_balances (user_id, balance)
  VALUES (p_user_id, 1000)
  ON CONFLICT (user_id) DO NOTHING;

  RETURN (SELECT balance FROM user_balances WHERE user_id = p_user_id);
END;
$$;

CREATE OR REPLACE FUNCTION set_user_balance(p_user_id UUID, p_balance INT)
RETURNS VOID
LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO user_balances (user_id, balance)
  VALUES (p_user_id, GREATEST(0, p_balance))
  ON CONFLICT (user_id)
  DO UPDATE SET balance = GREATEST(0, EXCLUDED.balance);
END;
$$;

CREATE OR REPLACE FUNCTION credit_user_balance(p_user_id UUID, p_amount INT)
RETURNS VOID
LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO user_balances (user_id, balance)
  VALUES (p_user_id, p_amount)
  ON CONFLICT (user_id) DO UPDATE
    SET balance = user_balances.balance + EXCLUDED.balance;
END;
$$;

