-- Convert legacy unique(user_id, market_id) constraint to partial unique index
DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM information_schema.table_constraints
    WHERE table_name = 'bets'
      AND constraint_type = 'UNIQUE'
      AND constraint_name = 'bets_user_id_market_id_key'
  ) THEN
    ALTER TABLE bets DROP CONSTRAINT bets_user_id_market_id_key;
  END IF;
END $$;

